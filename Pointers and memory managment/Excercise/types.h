#ifndef _TYPES_H
#define _TYPES_H
typedef unsigned char u8;       //%c
typedef signed char s8;         //%c
typedef unsigned short int u16; //%hu
typedef signed short int s16;   //%hi
typedef unsigned int u32;       //%u
typedef signed int s32;         //%d
typedef unsigned long int u64;  //%lu
typedef signed long int s64;    //%ld
typedef float f32;              //%f
typedef double f64;             //%lf
typedef long double f128;       //%Lf  NOTE: the difference between f64, and f128 is l and L
#endif